#include<iostream>

// функция для вывода на терминал значения аргументов
void Print_Char(int a =  'a', char b = 'b', char c = 'c', char d = 'd')
{
    std::cout << " a = " << a << '\n';
    std::cout << " b = " << b << '\n';
    std::cout << " c = " << c << '\n';
    std::cout << " d = " << d << '\n' << std::endl;
}
